package net.tretin.api;

import com.google.inject.Inject;
import com.google.inject.Injector;
import com.google.inject.name.Named;
import org.glassfish.jersey.server.spi.ContainerLifecycleListener;
import org.glassfish.jersey.servlet.ServletContainer;

public final class ApiServlet extends ServletContainer {
    @Inject
    public ApiServlet(
            ApiServletModule.Sources sources,
            Injector guice,
            @Named(ApiGuice.StartupBridgingListener) ContainerLifecycleListener listener) {
        getConfiguration().packages(sources.getPackages().toArray(new String[0]));
        getConfiguration().registerClasses(sources.getClasses().toArray(new Class<?>[0]));
        getConfiguration().register(listener);
    }
}
