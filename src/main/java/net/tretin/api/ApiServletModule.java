package net.tretin.api;

import com.google.inject.AbstractModule;

import java.util.Collections;
import java.util.LinkedList;
import java.util.List;

public final class ApiServletModule extends AbstractModule {
    List<String> getPakcages() {
        return pakcages;
    }

    private List<String> pakcages = new LinkedList<>();
    private List<Class<?>> classes = new LinkedList<>();

    public static ApiServletModule builder() {
        return new ApiServletModule();
    }

    public ApiServletModule addPackage(String name) {
        if (name == null || name.isEmpty()) {
            throw new IllegalArgumentException();
        }

        if (!pakcages.contains(name)) {
            pakcages.add(name);
        }

        return this;
    }

    public ApiServletModule addClass(Class<?> clazz) {
        if (clazz == null) {
            throw new IllegalArgumentException();
        }

        if (!classes.contains(clazz)) {
            classes.add(clazz);
        }

        return this;
    }

    @Override
    protected void configure() {
        bind(ApiServletModule.Sources.class).toInstance(
                new ApiServletModule.Sources() {
                    @Override
                    public List<String> getPackages() {
                        return Collections.unmodifiableList(pakcages);
                    }

                    @Override
                    public List<Class<?>> getClasses() {
                        return Collections.unmodifiableList(classes);
                    }
                }
        );
    }

    List<Class<?>> getClasses() {
        return classes;
    }

    interface Sources {
        List<String> getPackages();
        List<Class<?>> getClasses();
    }


}
