package net.tretin.api;

import com.google.inject.Inject;
import org.eclipse.jetty.server.HttpConfiguration;
import org.eclipse.jetty.server.HttpConnectionFactory;
import org.eclipse.jetty.server.Server;
import org.eclipse.jetty.server.ServerConnector;
import org.eclipse.jetty.servlet.ServletContextHandler;
import org.eclipse.jetty.servlet.ServletHolder;

public class ApiHttpServer {

    @Inject
    private Server server;

    @Inject
    private ApiServlet servlet;

    @Inject
    private ApiConfig config;

    public ApiHttpServer() {
        HttpConfiguration httpConfig = new HttpConfiguration();

        HttpConnectionFactory http = new HttpConnectionFactory(httpConfig);

        ServerConnector connector = new ServerConnector(server);
        connector.addConnectionFactory(http);

        ServletContextHandler api = new ServletContextHandler();
        api.setContextPath("/");
        api.addServlet(new ServletHolder(servlet), "/*");
    }

}
